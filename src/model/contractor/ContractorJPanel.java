    private void populateTable() {
        ArrayList<Material> mList = new ArrayList<>();
        MaterialSqlQuery msq = new MaterialSqlQuery();
        mList = msq.readAllMaterial();

        DefaultTableModel model = (DefaultTableModel) tblMaterial.getModel();
        model.setRowCount(0);

        for (Material e : mList) {
            Object row[] = new Object[10];
            row[0] = e.getMaterial_Name();
            row[1] = e.getMaterial_Category();
            row[2] = e.getDate_Of_Manufacture();
            row[3] = e.getManufacturer_Name();
            row[4] = e.getMaterial_Status();

            if ((e.getMaterial_Status().equals("Available"))) {
                model.addRow(row);
            }

        }
    }

    private void populateOrdersTable() {
        ArrayList<ContractorOrderBook> hobList = new ArrayList<>();
        Contractor_OrderBook_SqlQuery hsq = new Contractor_OrderBook_SqlQuery();
        hobList = hsq.readAllContractorOrderBook();

        ArrayList<Material> mList = new ArrayList<>();
        MaterialSqlQuery msq = new MaterialSqlQuery();
        mList = msq.readAllMaterial();

        DefaultTableModel model = (DefaultTableModel) tblContractorOrder.getModel();
        model.setRowCount(0);

        for (ContractorOrderBook e : hobList) {
            Object row[] = new Object[10];
            Material m = searchById(e.getMaterial_Id());
            row[0] = m.getMaterial_Name();
            row[1] = m.getMaterial_Category();
            row[2] = m.getManufacturer_Name();
            row[3] = e.getStatus();

            if ((e.getStatus().equals("Ordered"))) {
                model.addRow(row);
            }

        }
    }

    private Material searchById(int id) {
        Material m = null;
        ArrayList<Material> mList = new ArrayList<>();
        MaterialSqlQuery msq = new MaterialSqlQuery();
        mList = msq.readAllMaterial();
        for (Material e : mList) {
            if (e.getId() == id) {
                m = e;
            }
        }
        return m;
    }

    private String searchContractorById(int id) {
        String result = "";
        ArrayList<Contractor> mList = new ArrayList<>();
        ContractorSqlQuery msq = new ContractorSqlQuery();
        mList = msq.readAllContractor();
        for (Contractor h : mList) {

            if (h.getContractor_Id() == id) {
                System.out.println("mList->" + h);
                result = h.getContractor_Name();
                return result;
            }
        }
        return result;
    }

