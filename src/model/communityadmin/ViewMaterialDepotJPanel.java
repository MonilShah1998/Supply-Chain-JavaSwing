    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblMaterialDepot.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to edit");
            return;
        }
        ArrayList<MaterialDepot> mList =  new  ArrayList<>();
        MaterialDepotSqlQuery msq = new MaterialDepotSqlQuery();
        mList = msq.readAllMaterialDepot();
        MaterialDepot p = mList.get(selectedRow);
        
        msq.deleteMaterialDepot(p);
        
        
        
        populateTable();

        
    }//GEN-LAST:event_btnDeleteActionPerformed

