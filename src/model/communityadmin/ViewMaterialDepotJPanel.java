        private void populateTable() {
        ArrayList<MaterialDepot> mList =  new  ArrayList<>();
        MaterialDepotSqlQuery msq = new MaterialDepotSqlQuery();
        mList = msq.readAllMaterialDepot();
        
        
        DefaultTableModel model =(DefaultTableModel) tblMaterialDepot.getModel();
        model.setRowCount(0);
        
        for(MaterialDepot e: mList){
            System.out.println(e);
            Object row[]=new Object[10];
            row[0] = e.getMaterialDepot_Name();
            row[1] = e.getMaterialDepot_Location();
            row[2] = e.getMaterialDepot_Inventory_Size();
            row[3] = e.getEmail_Id();
            row[4] = e.getPassword();
            row[5] = e.getPhone_No();
            
            model.addRow(row);
            
        }
    }
